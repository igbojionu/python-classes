#Python for Loop:
#A for loop in Python is used to iterate (loop) over a sequence of elements, 
# such as a list, tuple, string, or range. It's like a repetitive task where 
# you perform the same operation for each item in the sequence.

#Example 1: Looping Through a List

fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)

#Example 2: Looping Through a Range
for i in range(5):
    print("Printing......")
    print(i)
